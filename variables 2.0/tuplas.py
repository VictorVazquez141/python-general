"""
Forma de crear una tupla
"""
#Tupla creada con tuple (toma como parametro una listra de datos)
tupla = tuple(["dato1","dato2","dato3"])
print(tupla)

#tupla creada con multiples datos y con parentesis
tupla2 = ("dato4","dato5","dato6")
print(tupla2)

#tupla creada con un solo dato y sin parentesis
tupla3 = "dato7",
print(tupla3)


